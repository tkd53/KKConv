# 機  能 : スクリプトのドキュメントを表示する。
#
# 実  例 : なし
#
# 注意点 : スクリプトにドキュメントが添付されていること。


#-------------------------------------------------------------------------------------
#                        declalations
#-------------------------------------------------------------------------------------

#-------------------------------------------------------------------------------------
#                        check arguments
#-------------------------------------------------------------------------------------

sub Help{
    $SCRIPT = shift;
    $/ = "\n\n\n";
    open(SCRIPT) || die;
    @line = <SCRIPT>;
    print STDERR $line[0], $line[$#line];
    close(SCRIPT);
    exit(0);
}


#-------------------------------------------------------------------------------------
#                        return
#-------------------------------------------------------------------------------------

1;


