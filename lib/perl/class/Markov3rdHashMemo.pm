#-------------------------------------------------------------------------------------
#                        declalations
#-------------------------------------------------------------------------------------

require "class/Markov3rdHash.pm";

package MarkovHashMemo;
@ISA = qw( MarkovHash );

use Carp;
use POSIX;
use DB_File;


#-------------------------------------------------------------------------------------
#                        set variables
#-------------------------------------------------------------------------------------

$SUFFIX = ".db";                                # ファイル名の拡張子


#-------------------------------------------------------------------------------------
#                        new
#-------------------------------------------------------------------------------------

# new(IntStr, SIZE)
# new(IntStr, SIZE, FILE)
#
# 機  能 : マルコフモデルのためのハッシュを生成する。

sub new{
    (@_ == 2) || (@_ == 3) || die;
    my($type, $size, $FILE) = @_;
    printf(STDERR "%s::new(%d, %s)\n", $type, $size, $FILE);

    my($self) = {};

    $$self{"___size___"} = $size;

    bless($self);

    $self->get($FILE) if (defined($FILE));

    return($self);
}


#-------------------------------------------------------------------------------------
#                        return
#-------------------------------------------------------------------------------------

1;


