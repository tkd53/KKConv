#-------------------------------------------------------------------------------------
#                        declalations
#-------------------------------------------------------------------------------------

require "class/SCFGHash.pm";

package SCFGHashMemo;
@ISA = qw( SCFGHash );

use Carp;
use POSIX;


#-------------------------------------------------------------------------------------
#                        set variables
#-------------------------------------------------------------------------------------

$SUFFIX = ".db";                                # ファイル名の拡張子


#-------------------------------------------------------------------------------------
#                        new
#-------------------------------------------------------------------------------------

# new(IntStr, SIZE)
# new(IntStr, SIZE, FILE)
#
# 機  能 : SCFG のためのハッシュを生成する。

sub new{
    (@_ == 2) || (@_ == 3) || die;
    my($type) = shift;
    my($intstr, $FILE) = (shift, shift);

    my($self) = {};

    bless($self);

    $self->get($FILE) if (defined($FILE));

    return($self);
}


#-------------------------------------------------------------------------------------
#                        return
#-------------------------------------------------------------------------------------

1;


