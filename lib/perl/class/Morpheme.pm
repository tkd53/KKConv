# 機  能 : けいたいそを扱うクラス
#
# 実  例 : なし
#
# 注意点 : 属性を返す関数が2つある。
#
#            attrib -> 品詞 品詞 句読点
#            Attrib -> 表記/品詞 表記/品詞 句読点
#


#-------------------------------------------------------------------------------------
#                        require
#-------------------------------------------------------------------------------------

#-------------------------------------------------------------------------------------
#                        declalations
#-------------------------------------------------------------------------------------

package Morpheme;


#-------------------------------------------------------------------------------------
#                        new
#-------------------------------------------------------------------------------------

# new(ARRAY)
#
# 機  能 : 形態素("表記/品詞")のリストを文節とみなしてインスタンスを生成する。
#
# 実  例 : $bunsetsu = new Bunsetsu(@list);
#
# 注意点 : 特になし

sub new{
    (@_ == 4) || die;
    my($type, $temp, $dest, $morp) = @_;

    return(bless([$morp, $dest]));
}


#-------------------------------------------------------------------------------------
#                        morp
#-------------------------------------------------------------------------------------

# morp
#
# 機  能 : 文節を構成する形態素列を返す。
#
# 実  例 : $morp->morp;
#
# 注意点 : "表記/品詞" である。

sub morp{
    (@_ == 1) || die;
    my($self) = @_;

    return($$self[0]);
}


#-------------------------------------------------------------------------------------
#                        dest
#-------------------------------------------------------------------------------------

# dest
#
# 機  能 : かかりさきを返す。
#
# 実  例 : $morp->dest;
#
# 注意点 : なし

sub dest{
    (@_ == 1) || die;
    my($self) = @_;

    return($$self[1]);
}


#-------------------------------------------------------------------------------------
#                        print
#-------------------------------------------------------------------------------------

# print
#
# 機  能 : インスタンスを表示する。
#
# 実  例 : $bunsetsu->print;
#
# 注意点 : 特になし

sub print{
    (@_ == 1) || die;
    my($self) = @_;

    print $self->sprint;
}


#-------------------------------------------------------------------------------------
#                        sprint
#-------------------------------------------------------------------------------------

# sprint
#
# 機  能 : インスタンスを文字列にして返す
#
# 実  例 : $morp->sprint
#
# 注意点 : 特になし

sub sprint{
    (@_ == 1) || die;
    my($self) = @_;

    sprintf("%d %s", $self->dest, $self->morp);
}


#-------------------------------------------------------------------------------------
#                        return
#-------------------------------------------------------------------------------------

1;


