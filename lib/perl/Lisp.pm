#-------------------------------------------------------------------------------------
#                        uniq
#-------------------------------------------------------------------------------------

# uniq(ARRAY)
#
# 機  能 : ARRAY の要素の重複を取り除く
#
# 実  例 : @array = uniq(@array);
#
# 注意点 : 順序も保存される。

sub uniq{
    my(%hash) = ();
    return(grep(!$hash{$_}++, @_));
}


#-------------------------------------------------------------------------------------
#                        plus
#-------------------------------------------------------------------------------------

# plus(ARRAY)
#
# 機  能 : ARRAY の要素の和を計算する。
#
# 実  例 : $sum = plus(@array);
#
# 注意点 : なし

sub plus{
    my($sum) = 0;

    $sum += $_ while ($_ = shift);

    return($sum);
}


