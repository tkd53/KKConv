# 機  能 : &min(VAL+) と &max(VAL+) を定義する。
#
# 実  例 : なし
#
# 注意点 : なし


#-------------------------------------------------------------------------------------
#                        declalations
#-------------------------------------------------------------------------------------


#-------------------------------------------------------------------------------------
#                        min
#-------------------------------------------------------------------------------------

sub min{
    my($min) = shift(@_);

    foreach (@_){
        ($_ < $min) && ($min = $_);
    }

    return($min);
}


#-------------------------------------------------------------------------------------
#                        max
#-------------------------------------------------------------------------------------

sub max{
    my($max) = shift(@_);

    foreach (@_){
        ($_ > $max) && ($max = $_);
    }
    
    return($max);
}


#-------------------------------------------------------------------------------------
#                        return
#-------------------------------------------------------------------------------------

1;


